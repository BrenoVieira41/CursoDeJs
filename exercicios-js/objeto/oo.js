// Codigo não executavel 

// Procedural
processamento(valor1, valor2, valor3)

// OO
objeto = { // objeto e a figura central do oo
  valor1, // o objeto tem seus atributos
  valor2,
  valor3,
  processamento() { // e tambem tem o processamento do objeto que seria a função que diz oque ele vai fazer
    // ...
  }
}
// e logo depois vem a maior diferença pois no lugar de ter uma função com dados
objeto.processamento() // tem os dados com funções
  
/** Principios importantes:
 * 1. abstracao = tem que abistrair o objeto simplificar o objeto no mundo real e trazer ele de forma simples pro sistema
 * 2. encapsulamento = a forma de esconder ou mostrar pro usuario correto oque ele pode ver
 * 3. herança = a forma de expecificar cada atributo com outro objeto mas sempre ligado com o objeto pai
 * 4. polimorfismo = criar um tipo e utilizar do mesmo
 */