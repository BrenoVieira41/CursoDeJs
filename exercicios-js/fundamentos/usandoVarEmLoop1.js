// for em loop se baseia em 3 partes
/**
 *  1 - Chamada da variavel
 *  2 - Condição em que o bloco vai ficar repetindo
 *  3 - Por ultimo o incremento
 */
for (var i = 0; i < 10; i++) { //aki ele diz que i = 0 ; enquanto i for menor que 10 ele manda ; e ele cria de um em um
  console.log(i)
}

console.log('i =', i) // Ele trara que i = o ultimo numero que fechou o laço no caso o 10