

console.log('01)', '1' == 1) //Dessa forma ele pergunta se e igual
console.log('02)', '1' === 1) //Dessa forma ele pergunta se e igual e se o tipo e o mesmo
console.log('03)', '3' != 3) //Aki ele pergunta se 3 e diferente de 
console.log('04)', '3' !== 3) //Dessa forma ele pergunta se e diferente e o tipo entra na contagem tambem

console.log('05)', 3 > 3) //Dessa forma ele pergunta se e maior 
console.log('06)', 3 >= 3) //Dessa forma ele pergunta se e maior ou igual
console.log('07)', 3 < 3) // Dessa forma ele pergunta se e merno 
console.log('08)', 3 <= 3) //Dessa forma ele pergunta se e menor ou igual 

